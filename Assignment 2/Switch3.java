
public class Switch3 {
	public static void main(String[] args) {
		int x=8;
    	int y=5;
	    int Operations= 2;
	    switch (Operations) {
	      case 1:
	    	System.out.println("Addition");
	        System.out.println(x+y);
	        break;
	      case 2:
	    	System.out.println("Subtraction");
	    	System.out.println(x-y);
	        break;
	      case 3:
	    	System.out.println("Multiplication");
	        System.out.println(x*y);
	      case 4:
	        System.out.println("Division");
	        System.out.println(x/y);
	        break;
	      
	    }
	  }
	

}
